export const translateDay = (day) => {
     switch(day) {
          case 'Monday':
               return 'Пн'
          case 'Tuesday':
               return 'Вт'
          case 'Wensday':
               return 'Ср'
          case 'Thursday':
               return 'Чт'
          case 'Friday':
               return 'Пт'
          case 'Saturday':
               return 'Сб'
          case 'Sunday':
               return 'Нд'
     }
}
