export const translateDay = (day) => {
     switch(day) {
          case 'Mon':
               return 'Пн'
          case 'Tue':
               return 'Вт'
          case 'Wed':
               return 'Ср'
          case 'Thu':
               return 'Чт'
          case 'Fri':
               return 'Пт'
          case 'Sat':
               return 'Сб'
          case 'Sun':
               return 'Нд'
     }
}
